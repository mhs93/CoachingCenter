    public function getSubject(Request $request)
    {

        $batch = Batch::where('id', $request->batchId)->select('subject_id')->first();
        $batchs = json_decode($batch->subject_id);
        $subjects = Subject::whereIn('id', $batchs)->get();
        return $subjects;
    }

